    private class ConfigureFocusScore implements Runnable {

        private Double mFocusScore;

        public ConfigureFocusScore(Double focusScore) {
            this.mFocusScore = focusScore;
        }

        @Override
        public void run() {
            mTextView.setText(String.format(FOCUS_SCORE_TEXT, mFocusScore));
            if (mFocusScore < MIN_FOCUS_SCORE) {
                mTextView.setBackgroundColor(0xFF0000);
            }
        }
    }
